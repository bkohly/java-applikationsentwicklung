public class Aufgabe11 {
    public static void main(String[] args) {
        System.out.println(Math.random()); //Wählt random double Zahl
        System.out.println(Math.PI); // Gibt den Wert von Pi(Mathematische Funktion) aus
        System.out.println(Math.sqrt(81)); // Zieht Wurzel aus der Zahl "81"
        System.out.println(Math.round(3.14)); //Rundet die Zahl
        System.out.println(Math.max(2343, 432)); //Gibt die höchste Zahl aus
    }
}
